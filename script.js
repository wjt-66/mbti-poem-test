let current = 0;
const count = { E: 0, I: 0, S: 0, N: 0, T: 0, F: 0, J: 0, P: 0 };

function render() {
    const app = document.getElementById('app');
    if (current >= data.questions.length) {
        const type = [
            count.E > count.I ? 'E' : 'I',
            count.S > count.N ? 'S' : 'N',
            count.T > count.F ? 'T' : 'F',
            count.J > count.P ? 'J' : 'P'
        ].join('');

        const poetInfo = poetDescriptions[type] || { poet: "未知诗人", description: "你的性格特质独特，难以用单一诗人概括。" };

        app.innerHTML = `
            <div class="result-container">
                <h2>你的MBTI类型:${type}</h2>
                <div class="poet-match">
                    <h3>与你最相似的诗人：${poetInfo.poet}</h3>
                    <p class="poet-description">${poetInfo.description}</p>
                </div>
                <div class="restart-btn" onclick="location.reload()">重新测试</div>
            </div>
        `;
        return;
    }

    const q = data.questions[current];
    app.innerHTML = `
        <div class="progress">${current+1}/20</div>
        <div class="question">${q.text}</div>
        <div class="option" onclick="select('A')">A 同意</div>
        <div class="option" onclick="select('B')">B 不同意</div>
    `;
}

function select(ans) {
    const q = data.questions[current];
    count[ans === 'A' ? q.scoreA : q.scoreB]++;
    current++;
    render();
}

